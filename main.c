/*
MULTILINE COMMENTS
used to explain the documentation
*/

#include <stdio.h>
#include <stdlib.h>

int main(int argc, char const* argv[]) {
	// Single line comments
	printf("Hello World\n");

	// Variables
	int a = 12;
	char b[] = "b";
	float c = 2.3;
	double d = 23.45;

	// Logic operators
	int e = 2;
	int f = 3;
	printf("%d", (e ^ f));

	return 0;
}
